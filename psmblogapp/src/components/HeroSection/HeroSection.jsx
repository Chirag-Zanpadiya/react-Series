import React, { useState } from "react";
import { Link, useNavigate } from "react-router-dom";
import { useSelector } from "react-redux";
import Button from "../Button/Button";

function HeroSection() {
  const [activeIndex, setActiveIndex] = useState(0);

  // ye feature basically pop dene ke liye hai ki agrar user login nahi hai 
  // read post karna chahat hai to first of all login karna padega
  const [showPopup, setShowPopup] = useState(false);

  // user ka status leke ayege store me se
  const isLoggedIn = useSelector((state) => state.auth?.status);
  const navigate = useNavigate();

  const slides = [
    {
      imgSrc: "/fullstack.jpg",
      title: "Mastering Web Development: From Basics to Advanced",
      price: "The ultimate roadmap to becoming a web developer! Covers everything from HTML, CSS, JavaScript to React.js.",
      rating: 4.5,
      slug: "/post/mastering-web-development",
    },
    {
      imgSrc: "/leetcode.jpeg",
      title: "LeetCode vs CodeChef vs HackerRank: Which is Best?",
      price: "Which platform is the best for learning DSA and competitive programming? A detailed comparison!",
      rating: 5.0,
      slug: "/post/leetcode-vs-codeforce-vs-hackerrank",
    },
    {
      imgSrc: "/mistake.png",
      title: "Top 10 Mistakes Beginner Developers Make (And How to Avoid Them)",
      price: "Common coding mistakes that new developers make! Learn how to avoid them and improve your skills.",
      rating: 5.0,
      slug: "/post/top-10-mistake-beginers-make",
    },
    // Add more slides as needed
  ];

  const handlePrev = () => {
    setActiveIndex((prevIndex) =>
      prevIndex === 0 ? slides.length - 1 : prevIndex - 1
    );
  };

  const handleNext = () => {
    setActiveIndex((prevIndex) =>
      prevIndex === slides.length - 1 ? 0 : prevIndex + 1
    );
  };


  
  const handleReadMore = (slug) => {
    // agar user login hai toh user post per navigate kara dege
    if (isLoggedIn) {
      navigate(slug);


    }
    
    // agar user login nahi hai toh 
    // pop status true kar dege 
    // showpop true ho jayega toh pop msg ayega last div seee
    else {
      setShowPopup(true);
    }
  };

  return (
    <div
      id="indicators-carousel"
      className="relative w-full min-h-screen pb-8 bg-black"
    >
      <div className="min-h-screen overflow-hidden rounded-xl md:h-96">
        {slides.map((slide, index) => (
          <div
            key={index}
            className={`duration-700 ease-in-out ${
              index === activeIndex ? "block" : "hidden"
            }`}
            data-carousel-item={index === activeIndex ? "active" : ""}
          >
            <div className="mt-24 relative max-w-5xl mx-auto bg-black flex justify-center items-center">
              <div className="w-full max-w-5xl min-h-[1024px] bg-white border border-gray-200 rounded-lg shadow-sm dark:bg-gray-800 dark:border-gray-700">
                <a href="#" className="flex justify-center items-center ">
                  <img
                    className="object-contain py-4 px-4 h-[461px] min-w-full"
                    src={slide.imgSrc}
                    alt="product image"
                  />
                </a>
                <div className="px-5 relative">
                  <h5 className="text-3xl font-bold tracking-tight text-gray-900 dark:text-white">
                    {slide.title}
                  </h5>

                  <div className="flex items-center mt-4 justify-between">
                    <span className="text-xl max-w-xl text-semibold text-gray-700 dark:text-gray-200">
                      {slide.price}
                    </span>
                    <Button
                      onClick={() => handleReadMore(slide.slug)}
                      className="text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-blue-300 font-medium rounded-lg text-sm px-5 py-2.5 text-center dark:bg-blue-600 dark:hover:bg-blue-700 dark:focus:ring-blue-800"
                    >
                      Read More
                    </Button>
                  </div>

                  <div className="flex items-center mt-2.5 mb-5">
                    <div className="flex items-center space-x-1 rtl:space-x-reverse">
                      {[...Array(5)].map((_, i) => (
                        <svg
                          key={i}
                          className={`w-4 h-4 ${
                            i < slide.rating
                              ? "text-yellow-300"
                              : "text-gray-200 dark:text-gray-600"
                          }`}
                          aria-hidden="true"
                          xmlns="http://www.w3.org/2000/svg"
                          fill="currentColor"
                          viewBox="0 0 22 20"
                        >
                          <path d="M20.924 7.625a1.523 1.523 0 0 0-1.238-1.044l-5.051-.734-2.259-4.577a1.534 1.534 0 0 0-2.752 0L7.365 5.847l-5.051.734A1.535 1.535 0 0 0 1.463 9.2l3.656 3.563-.863 5.031a1.532 1.532 0 0 0 2.226 1.616L11 17.033l4.518 2.375a1.534 1.534 0 0 0 2.226-1.617l-.863-5.03L20.537 9.2a1.523 1.523 0 0 0 .387-1.575Z" />
                        </svg>
                      ))}
                    </div>
                    <span className="bg-blue-100 text-blue-800 text-xs font-semibold px-2.5 py-0.5 rounded-sm dark:bg-blue-200 dark:text-blue-800 ms-3">
                      {slide.rating}
                    </span>
                  </div>
                </div>
              </div>
            </div>
          </div>
        ))}
      </div>

      <br />

      <div className="absolute z-30 flex -translate-x-1/2 space-x-3 rtl:space-x-reverse bottom-5 left-1/2">
        {slides.map((_, index) => (
          <button
            key={index}
            type="button"
            className={`w-3 h-3 rounded-full ${
              index === activeIndex ? "bg-blue-600" : "bg-gray-300"
            }`}
            aria-current={index === activeIndex ? "true" : "false"}
            aria-label={`Slide ${index + 1}`}
            onClick={() => setActiveIndex(index)}
          ></button>
        ))}
      </div>

      <button
        type="button"
        className="absolute ml-5 top-1/2 hidden lg:flex start-0 z-15 items-center justify-center h-14 w-14 px-2 cursor-pointer group focus:outline-none"
        onClick={handlePrev}
      >
        <span className="inline-flex items-center justify-center w-10 h-10 rounded-full bg-white/30 dark:bg-blue-800/30 group-hover:bg-gray-300/50 dark:group-hover:bg-gray-800/60 group-focus:ring-4 group-focus:ring-white dark:group-focus:ring-gray-800/70 group-focus:outline-none">
          <svg
            className="w-4 h-4 text-white dark:text-blue-800 rtl:rotate-180"
            aria-hidden="true"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 6 10"
          >
            <path
              stroke="currentColor"
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth="2"
              d="M5 1 1 5l4 4"
            />
          </svg>
          <span className="sr-only">Previous</span>
        </span>
      </button>

      <button
        type="button"
        className="absolute mr-5 top-1/2 hidden lg:flex end-0 z-15 items-center justify-center h-14 w-14 px-2 cursor-pointer group focus:outline-none"
        onClick={handleNext}
      >
        <span className="inline-flex items-center justify-center w-10 h-10 rounded-full bg-white/30 dark:bg-blue-800/30 group-hover:bg-gray-300/50 dark:group-hover:bg-gray-800/60 group-focus:ring-4 group-focus:ring-white dark:group-focus:ring-gray-800/70 group-focus:outline-none">
          <svg
            className="w-4 h-4 text-white dark:text-blue-800 rtl:rotate-180"
            aria-hidden="true"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 6 10"
          >
            <path
              stroke="currentColor"
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth="2"
              d="m1 9 4-4-4-4"
            />
          </svg>
          <span className="sr-only">Next</span>
        </span>
      </button>


        {/* agar status true hai toh pop msg dekhayege  */}
        {/* nahi hai toh mat dikho */}
      {showPopup && (
        <div className="fixed inset-0 flex items-center justify-center bg-black bg-opacity-50">
          <div className="bg-white p-6 rounded-lg shadow-lg">
            <h2 className="text-xl font-bold mb-4">Please Log In</h2>
            <p className="mb-4">You need to be logged in to read more.</p>
            <Button
              onClick={() => setShowPopup(false)}
              className="bg-blue-500 text-white px-4 py-2 rounded"
            >
              Close
            </Button>
          </div>
        </div>
      )}
    </div>
  );
}

export default HeroSection;